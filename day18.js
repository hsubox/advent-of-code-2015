const assert = require('assert');

function readInput(input) {
  return input.split('\n').map((row) => {
    return row.split('').map((item) => {
      return (item == '#') ? 1 : 0;
    });
  });
}

function determineNeighbors([x, y], lightGrid) {
  const allNeighbors = [[x - 1, y - 1], [x, y - 1], [x + 1, y - 1], [x - 1, y], [x + 1, y], [x - 1, y + 1], [x, y + 1], [x + 1, y + 1]];
  return allNeighbors.filter(([x, y]) => {
    return !(x < 0 || x >= lightGrid.length || y < 0 || y >= lightGrid[0].length);
  })
}

function calculateNextLightGrid(lightGrid, cornersOn = false) {
  let nextLightGrid = lightGrid.map((row, x) => {
    return row.map((light, y) => {
      let neighbors = determineNeighbors([x, y], lightGrid).filter(([a, b]) => {
        return lightGrid[a][b] == 1;
      }).length;
      if (neighbors == 3) {
        return 1;
      }
      if (light == 1 && neighbors == 2) {
        return 1;
      }
      return 0;
    });
  });
  if (cornersOn) {
    nextLightGrid[0][0] = 1;
    nextLightGrid[0][lightGrid[0].length - 1] = 1;
    nextLightGrid[lightGrid.length - 1][0] = 1;
    nextLightGrid[lightGrid.length - 1][lightGrid[0].length - 1] = 1;
  }
  return nextLightGrid;
}

function calculateNLightGrid(initialLightGrid, n, cornersOn = false) {
  let lightGrid = initialLightGrid;
  for (let i = 0; i < n; i++) {
    lightGrid = calculateNextLightGrid(lightGrid, cornersOn);
  }
  return lightGrid;
}

function displayLightGrid(lightGrid) {
  return lightGrid.map((row) => row.map((x) => (x) ? '#' : '.').join('')).join('\n');
}

function countLightsOn(lightGrid) {
  return lightGrid.reduce((sumOuter, row) => {
    return sumOuter + row.reduce((sumInner, item) => {
      return sumInner + item;
    });
  }, 0);
}

